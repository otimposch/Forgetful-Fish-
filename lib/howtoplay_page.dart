## **戦略〜ダンダーンゲームの流儀〜**
### **【戦略1.ドローを制するものはゲームを制す】**
両プレイヤーが同じデッキを共有するため、ドローをコントロールすることは、ゲームをコントロールすることになります。\n
ライブラリーの一番上のコントロールし、（自分にも十分な土地があることが前提ですが）自分には最高のカードを相手には土地を与えます。
使われると厄介なカードや、使うことでアドバンテージを得るようなカードには、《記憶の欠落》を打って逆に自分のものにしてしまいましょう。\n
カード・アドバンテージは大きな鍵であり、《蓄積された知識》のコントロールは《ダンダーン》や《記憶の欠落》と同様に重要です。
どうしてもライブラリーの一番上のカードを引く必要があるのでなければ、手札にあるものを唱える前に、少なくとも墓地に《蓄積された知識》が1枚落ちるまで待ちたいところです。
カード・アドバンテージを著しく失っていたり、ダメージから身を守るための他の選択肢がなかったりする場合は、《先細りの収益》で手札をリセットするようにしましょう。\n
《蓄積された知識》や《記憶の氾濫》、サイクリングランドは、あなたや対戦相手がライブラリーの一番上にあるカードを知っていて、
あなたがそれを欲しい(または欲しくない)とき、あるいは対戦相手にドローされたくないときのために取っておくようにしましょう。\n
《予報》は、対戦相手が自分で引けるようにセットしたカードを引かせたくないときに取っておくとよいでしょう。
なお、《予報》のカードを指定するのは解決時であり、唱えたときではありません。一番上のカードが何なのかわからない場合は、《島》がデッキの中で一番多いカードです。

### **【戦略2.ダンダーンを支配しろ】**
ForgetfulFishを初めてプレイするときは分かりにくいですが、《ダンダーン》を死亡させる鍵は、「生息条件(島)」です。\n
対戦相手が島をコントロールしていない状態にするか（《幻視の魔除け》）、
《ダンダーン》のテキストを変える（例えば生息条件を"森"に変える）ことで（《水晶のしぶき》や《幻覚》）破壊できます。\n
《幻視の魔除け》はライブラリーの一番上をクリアするのにも使えますが、神の怒り(全体除去)として使う方が有用なことが多いです。
なお、《幻視の魔除け》はすべての《ダンダーン》の死亡を誘発させるので、《幻視の魔除け》に《記憶の欠落》などのカウンターを打つか、
《ダンダーン》を手札に戻すか、《空智の踊り》で《ダンダーン》から能力を奪う必要があります。\n
《ダンダーン》の生息条件による死亡が誘発された後、またはそれに反応して《水晶のしぶき》や《幻覚》で《ダンダーン》のテキストを変更しても
、それは「場合のルール('intervening if' rule)」の不正対象には該当せず、能力の解決時に《ダンダーン》は生け贄となるため救えません。（規則608.2a）\n
また、《幻覚》、《水晶のしぶき》、《幻視の魔除け》の文章変更の選択は、解決時に行われるのであって、唱える時に選ばれるのではないことに注意してください。
したがって、それらの呪文の解決前に、1つの土地のタイプを変更したり、《ダンダーン》のテキストを変更したりして《ダンダーン》の生贄を回避しようとすることは無意味です。
それを見たプレイヤーは別の選択をすればいいだけだからです。\n
ゲーム開始時の手札に基本でない土地が2枚ほど含まれている場合、数ターンの間、島をプレイしないことを検討し、《ダンダーン》があなたを攻撃できないようにして、
自分の《ダンダーン》を戦場に出し始めるときに有利になるカードを集めることができるかもしれません。この短期的な戦略のために、基本土地を出すことを何度も見送る必要はありません。 

### **【戦略3.記憶の欠落を逆に利用しろ】**
相手の終了ステップにマナを用意できずに、やむをえず自分のアップキープ中にインスタント呪文を唱えることになっても、
相手がそれに《記憶の欠落》を唱えた場合、自分のドローステップに再びその呪文を引けるため有益な場合があります。\n
ドロー・ステップの直前に《神秘の教示者》を唱えることも有効です。
同様に、相手が《記憶の欠落》を使った場合に備えて、重要な呪文を唱えるのは相手のドロー・ステップの後まで待った方が得策でしょう。

### **【戦略4.バウンスのタイミングに気をつけろ】**
対戦相手がタップアウトしていない限り、スタックにある対戦相手の呪文に《非実在化》を唱えても、再キャストすることができてしまうため、それはあまり有効な手段ではありません。\n
その代わり、ブロッカーをバウンスしたり、《ダンダーン》を破壊から救ったり、あるいは《記憶の欠落》されそうな自分の呪文を戻して、もう一度唱えたりするために取っておくのがよいでしょう。

### **【戦略5.ライブラリアウトに備えろ】**
ゲームが長引き、プレイヤーが《先細りの収益》をうまく唱えられなかった場合（あるいは唱えすぎた場合）、どちらが先にライブラリアウトを回避するかに帰結するかもしれません。\n
デッキがほとんどなくなってしまった場合、デッキにカードがない状態でカードを引かざるを得なくなったとき、自分の呪文に《記憶の欠落》を撃つなど、カードをライブラリーに置くためのトリックを1つか2つ用意しておきましょう。


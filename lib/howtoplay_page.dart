## **基本ルール**
### ForgetfulFishは、以下の簡単なルール変更があるだけで通常のマジックと同じようにプレイします。        
- プレイヤーはライブラリーと墓地を共有します。両プレイヤーの手の届く位置(両者の間）に置きます。ライブラリーや墓地の「オーナー」を参照したり、あなたや対戦相手の「ライブラリー」や「墓地」を参照したりする効果は、共通のライブラリーや共通の墓地を参照します。カードの「所有者」は、手札からそのカードをプレイした人を指します。
- ゲーム開始時の手札や、両プレイヤーが同じ効果から同時にカードを引く必要がある場合には、通常プレイと同様に、APNAP(アクティブプレイヤー・ノンアクティブプレイヤー)の原則に従います。カードを配るときも同様にAP→NAPの順にデッキの一番上からカードを配ります。ディールはアクティブプレイヤーが行います。
- マリガンは通常のルールが適用されますが、開始時の7枚の手札に土地カードが2枚以上含まれていない場合、もしくは6枚以上含まれている場合は、そのプレイヤーはそれらのカードを公開し、フリーマリガンをすることができます。（条件を満たしていない場合は、通常のマリガンになります。）
- ゲームは通常通り20点のライフで開始します。※ほとんどの場合、ダンダーン以外のダメージソースはないのでライフの合計を5つのカウンターや、6面のダイス1つで表すことも可能です(ライフはダンダーンのアタックでしか失わないため)
- ライブラリアウトによる敗北ルールも通常通りです。ただし、ライブラリが共通であることをお忘れなく。
- スタックも通常通り機能しますが、ゲームの性質上、非常に複雑になる可能性があります（それがこのゲームの醍醐味なのですが）そのためプレイヤーは対戦相手が反応するまでの時間を確保し、スタックの解決を始める前、呪文が解決されるたび、ライブラリーの一番上からカードを引く前に、対戦相手が優先権を通過することを確認することが重要です。


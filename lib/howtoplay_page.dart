## **カード別関連ルール説明**
### カード別に間違いやすい効果などについて説明を行います。プレイに慣れている方は読み飛ばしても構いません。
- **サイクリング・ランド**：サイクリングは起動型能力であり、呪文ではありません。スタックに乗りますが、《記憶の欠落》等のカウンターによって打ち消されることはありません。
- **蓄積された知識**：このカウントには、現在解決中のカードは含まれません。そのカードが墓地に行くのは、そのカードの効果を解決した後です（つまり初回は1枚しかドローできません）
- **記憶の氾濫**：記載されている操作はすべて1つの効果です。あなたは1回の解決で3枚のカードを引き、2枚のカードを戻します。その間には何も起こりません。デッキにはこの効果で引いたカード以外を戻しても構いません。
- **水晶のしぶき、幻覚**：これら呪文は、効果で変更可能な言葉が書かれていないカードや、バニラ(効果が書かれていないカード)も対象とすることができます。
これら呪文は、対象となったカードのテキストボックスとタイプ行にある、すべての単語を選ばれた単語に変更します。
変化させる単語は、唱えるときではなく解決時に選択します。単語を同じ単語に変更することはできません(異なる単語でなければなりません)。
カードに印刷されているもの（またはトークンが作成されたときやコピー効果によってセットされたもの）だけを変更します。
パーマネントになる呪文のテキストを変更した場合、そのパーマネントは効果が切れるまでテキストは変更されたままです。
土地のタイプを、ある基本土地タイプから別のタイプに変更することもできます。例えば、カードタイプ《島》をカードタイプ《山》に変更できます。
そのとき、土地は赤マナを出すことになりますが、名前そのものを変えることできず、カード名は《山》のままです。（カード名の変更は、どのパーマネントに対してもできません）
- **先細りの収益**： このカードがあなたの墓地に置かれるのは、このカードの解決が終わった後であり、つまり、それ自身が効果の一部としてライブラリー戻りシャッフルされ、墓地が空になることはありません。
カードを何枚引くかを選択する前に表向きでカードを追放することになるため、先に追放されるカードを確認します。各プレイヤーは、0から7までの任意の枚数のカードを引くことを選択できます。
まずアクティブプレイヤーがカードを何枚引くかを選び、次にノンアクティブプレイヤーが選びます。その後、カードを引きます。
ForgetfulFishでは、アクティブなプレイヤーは、ポーカーや花札のカードを配るように、アクティブプレイヤーが、非アクティブなプレイヤーから交互に1枚ずつカードを配る必要があります。
- **ハリマーの深み-**： この能力は、それが戦場に出たときに誘発し、他の誘発型能力のようにスタックに乗ります。
- **腹黒い意志**：あなたはコピーされた呪文をコントロールします。あなたが対象の呪文やコピーの新しい対象を選ぶ場合、その新しい対象は適正な対象でなければなりません。
スタックにある呪文は、自分自身を対象には取れません。コピーされた呪文が、モードを選択する呪文であった場合、コピーも同じ選択されたモードを持ちます。
- **記憶の欠落**： 記憶の欠落は、他の効果がそのイベントを置き換えることができる前に、墓地に行く呪文を置き換える自己置換効果を持つ。ただし、その呪文がフラッシュバックを使って唱えられたものである場合、フラッシュバックはその呪文の行き先をオーナーのライブラリーから追放に変更する。
- **神秘の教示者**： インスタントやソーサリーのカードを探したくなければ、探さなくてよいです。
- **神秘の回復**： 墓地からフラッシュバックで唱えられた《神秘の回復》は、それ自身を対象にすることはできません。
それは、あなたがその対象を選ぶときにスタックにあります。フラッシュバックで唱えた呪文は、《記憶の欠落》のようなデッキトップに戻る効果の受けたとしても、行き先が変更され、追放されます。
- **神秘の聖域**： 島とは、島というサブタイプを持つ土地のことであり、青マナを生み出せる土地のことではありません。例えば、《神秘の聖域》は《島》ですが、《ハリマーの深み》は《島》ではありません。
- **予報**：名前の指定は唱えたときではなく、解決時に行います。
- **命令の光**： アンタップされているクリーチャー1体を対象としても構いません。そのクリーチャーは、クリーンアップ・ステップ中に「ターン終了時まで」の効果が切れたとき、対戦相手のコントロールに戻ります(そしてタップされます)。
このカードの効果が終了したとき、あるいは呪文や能力によって他のプレイヤーがそのクリーチャーのコントロールを得たときなど、
何らかの理由であなたがそのクリーチャーのコントロールを失ったとき、あなたはそのクリーチャーをタップします。\n
たとえば《命令の光》で奪ったクリーチャーに対して、対戦相手がさらに《命令の光》を使って、そのクリーチャーのコントロールを取り戻した場合、
そのクリーチャーは、2枚目の《命令の光》が解決された後(つまりアンタップされた後)、1枚目の《命令の光》の誘発型能力によりタップされます。
- **奪取の形態**： トークンは、そのクリーチャーに印刷されていたものをそのままコピーし、それ以外のものはコピーしません。
対象にとったクリーチャーがトークンである場合、《奪取の形態》が生み出すトークンは、対象としたトークンを戦場に出した効果によって定義された元の特性をそのままコピーします。
対象としたクリーチャーのタップ/アンタップの状態や、何らかのカードの効果により修正されたパワー、タフネス、タイプ、色などはコピーしません。
- **Temple of Epiphany**: この場合のScryは、Temple of Epiphanyがプレイされたときにスタックを開始する誘発型能力である。
- **幻視の魔除け**： 変化させる土地と、それが何に変化するかは、唱えるときではなく解決時に選びます。変更された土地はすべて同じタイプに変更され、選ばれた色のマナを生成します。

